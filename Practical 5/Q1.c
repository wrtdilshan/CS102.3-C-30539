//While loop

#include <stdio.h>
int main()
{
int x=0;
while (x <= 100)
{
printf("%d \n",x);
x++ ;
}
}

//Do While

#include <stdio.h>
int main()
{
int x = 0;
do
{
printf("%d \n", x);
x++ ;
}
while (x<=100);
}

//For loop

#include <stdio.h>
int main ()
{
int x;
for(x=0; x<=100; x++ )
printf("%d \n", x);
}
